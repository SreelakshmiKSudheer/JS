var a; // declare
var b = 2; // initialize

a = 7; // assign
console.log("a = ", a);
console.log("b = ", b);

// arithmetic
var result;
result  = a + b; // add
console.log("a + b = ", result);

result  = a - b; // sub
console.log("a - b = ", result);

result  = a * b; // mul
console.log("a * b = ", result);

result  = a / b; // div
console.log("a / b = ", result);

result  = a % b; // div
console.log("a mod b = ", result);

// pre
console.log(++a);
console.log(--a);
// post
console.log(a++);
console.log(a--);

// arithmetic-assignment operatoers in C is also used in JS